number = float(input("Introduce un número: "))
integer = int(input("Introduce un número entero: "))
text = input("Introduce una cadena: ")
print("Número =", number)
print("Tipo de la variable número:", type(number))
print("Número * 2 =", number * 2)
print("Entero =", integer)
print("Tipo de la variable entero:", type(integer))
print("Entero * 2 =", integer * 2)
print("Cadena =", text)
print("Tipo de la variable cadena:", type(text))
print("Cadena * 2 =", text * 2)

